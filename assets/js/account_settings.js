function toggle_password0(target){
    var d = document;
    var tag = d.getElementById(target);
    var tag2 = d.getElementById("showhide0");
    
    if (tag2.innerHTML == 'Show'){
        tag.setAttribute('type', 'text');   
        tag2.innerHTML = 'Hide';

    } else {
        tag.setAttribute('type', 'password');   
        tag2.innerHTML = 'Show';
    }
}

